# Se asigna el valor binario 100101 a la variable 'h' utilizando la notación 0b para indicar que es binario
h = 0b100101

# Se imprime el mensaje "Binario a Hexadecimal", seguido del número en binario (h), y luego los dos puntos
# Luego se imprime el equivalente hexadecimal utilizando la función hex()
print("Binario a Hexadecimal", h, ":", hex(h))
